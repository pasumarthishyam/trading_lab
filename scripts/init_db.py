def create_schema(conn):
    """Create all tables."""
    conn.executescript(_SCHEMA)
    print("  [OK]  Schema created (7 tables)")


def seed_data(conn):
    """Insert seed data (strategies, instruments, categories, tests)."""

    # Strategies
    for name, slug, desc in _SEED_STRATEGIES:
        conn.execute(
            "INSERT OR IGNORE INTO strategies (name, slug, description) "
            "VALUES (?, ?, ?)",
            (name, slug, desc),
        )
    print("  [OK]  Strategies seeded")

    # Instruments
    for name, symbol, asset_type in _SEED_INSTRUMENTS:
        conn.execute(
            "INSERT OR IGNORE INTO instruments (name, symbol, asset_type) "
            "VALUES (?, ?, ?)",
            (name, symbol, asset_type),
        )
    print("  [OK]  Instruments seeded")

    # Categories — look up strategy_id by slug
    for strat_slug, name, slug, order in _SEED_CATEGORIES:
        row = conn.execute(
            "SELECT id FROM strategies WHERE slug = ?", (strat_slug,)
        ).fetchone()
        if row:
            conn.execute(
                "INSERT OR IGNORE INTO test_categories "
                "(strategy_id, name, slug, display_order) VALUES (?, ?, ?, ?)",
                (row["id"], name, slug, order),
            )
    print(f"  [OK]  {len(_SEED_CATEGORIES)} categories seeded")

    # Tests — look up category_id by slug
    inserted = 0
    for cat_slug, num, name, slug, desc in _SEED_TESTS:
        row = conn.execute(
            "SELECT id FROM test_categories WHERE slug = ?", (cat_slug,)
        ).fetchone()
        if row:
            conn.execute(
                "INSERT OR IGNORE INTO tests "
                "(category_id, test_number, name, slug, description) "
                "VALUES (?, ?, ?, ?, ?)",
                (row["id"], num, name, slug, desc),
            )
            inserted += 1
    print(f"  [OK]  {inserted} tests seeded")

    conn.commit()


def reset_db():
    """Delete the database file entirely."""
    db_path = get_db_path()
    if db_path.exists():
        db_path.unlink()
        # Also remove WAL and SHM files if present.
        for suffix in ("-wal", "-shm"):
            p = db_path.parent / (db_path.name + suffix)
            if p.exists():
                p.unlink()
        print(f"  [OK]  Deleted {db_path}")
    else:
        print(f"  [--]  No database found at {db_path}")


def main():
    parser = argparse.ArgumentParser(
        description="Initialize the Trading Lab SQLite database"
    )
    parser.add_argument(
        "--reset", action="store_true",
        help="Delete existing database before recreating",
    )
    args = parser.parse_args()

    print("=" * 55)
    print("  Trading Lab — Database Initialization")
    print("=" * 55)

    if args.reset:
        print("\n  Resetting database...")
        reset_db()

    print(f"\n  Database: {get_db_path()}")

    conn = get_connection()
    try:
        create_schema(conn)
        seed_data(conn)

        # Verify
        counts = {}
        for table in ["strategies", "instruments", "test_categories", "tests"]:
            row = conn.execute(f"SELECT COUNT(*) as c FROM {table}").fetchone()
            counts[table] = row["c"]

        print("\n  Verification:")
        for table, count in counts.items():
            print(f"    {table:20s}  {count} rows")

        print("\n" + "=" * 55)
        print("  Done. Database ready.")
        print("=" * 55)
    finally:
        conn.close()


if __name__ == "__main__":
    main()

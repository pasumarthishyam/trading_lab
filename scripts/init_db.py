_SCHEMA = """
-- Strategies (VCF, future strategies)
CREATE TABLE IF NOT EXISTS strategies (
    id            INTEGER PRIMARY KEY AUTOINCREMENT,
    name          TEXT NOT NULL,
    slug          TEXT NOT NULL UNIQUE,
    description   TEXT,
    created_at    TEXT DEFAULT (datetime('now'))
);

-- Instruments a strategy can be tested against
CREATE TABLE IF NOT EXISTS instruments (
    id            INTEGER PRIMARY KEY AUTOINCREMENT,
    name          TEXT NOT NULL,
    symbol        TEXT NOT NULL UNIQUE,
    asset_type    TEXT NOT NULL
);

-- Test categories within a strategy
CREATE TABLE IF NOT EXISTS test_categories (
    id            INTEGER PRIMARY KEY AUTOINCREMENT,
    strategy_id   INTEGER NOT NULL REFERENCES strategies(id),
    name          TEXT NOT NULL,
    slug          TEXT NOT NULL,
    display_order INTEGER NOT NULL,
    UNIQUE(strategy_id, slug)
);

-- Individual test definitions
CREATE TABLE IF NOT EXISTS tests (
    id            INTEGER PRIMARY KEY AUTOINCREMENT,
    category_id   INTEGER NOT NULL REFERENCES test_categories(id),
    test_number   INTEGER NOT NULL,
    name          TEXT NOT NULL,
    slug          TEXT NOT NULL,
    description   TEXT,
    UNIQUE(category_id, test_number)
);

-- Each run of a test against an instrument
CREATE TABLE IF NOT EXISTS test_runs (
    id                  INTEGER PRIMARY KEY AUTOINCREMENT,
    test_id             INTEGER NOT NULL REFERENCES tests(id),
    instrument_id       INTEGER NOT NULL REFERENCES instruments(id),
    run_at              TEXT NOT NULL,
    duration_seconds    REAL,
    status              TEXT NOT NULL DEFAULT 'running',
    data_window_start   TEXT,
    data_window_end     TEXT,
    config_snapshot     TEXT,
    row_count           INTEGER,
    terminal_output     TEXT,
    notes               TEXT
);

-- Key findings from each run
CREATE TABLE IF NOT EXISTS insights (
    id              INTEGER PRIMARY KEY AUTOINCREMENT,
    test_run_id     INTEGER NOT NULL REFERENCES test_runs(id),
    insight_key     TEXT NOT NULL,
    insight_value   TEXT,
    insight_text    TEXT NOT NULL,
    severity        TEXT DEFAULT 'info',
    created_at      TEXT DEFAULT (datetime('now'))
);

-- Charts generated by each run
CREATE TABLE IF NOT EXISTS charts (
    id              INTEGER PRIMARY KEY AUTOINCREMENT,
    test_run_id     INTEGER NOT NULL REFERENCES test_runs(id),
    filename        TEXT NOT NULL,
    title           TEXT,
    chart_type      TEXT,
    description     TEXT,
    plotly_json     TEXT
);

-- Data tables generated by each run
CREATE TABLE IF NOT EXISTS data_tables (
    id              INTEGER PRIMARY KEY AUTOINCREMENT,
    test_run_id     INTEGER NOT NULL REFERENCES test_runs(id),
    filename        TEXT NOT NULL,
    title           TEXT,
    description     TEXT,
    row_count       INTEGER,
    column_count    INTEGER,
    data_json       TEXT
);
"""


